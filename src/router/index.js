const router = new VueRouter({
  mode: 'history',
  base: process.env.BASE_URL,
  routes
});

router.beforeEach((to,from,next)=>{
  const openRouter=["Login","Register","Verify","Resendcode","Recoverpwd"]
  const isAuthenticated=JSON.parse(localStorage.getItem("authentication"));
  if(openRouter.includes(to.name)){
    next()
  }else if(isAuthenticated){
    next()
  }
  else if(to.name===openRouter.includes(to.name) && isAuthenticated){
    next({name:"Home"})
  }
  else{
    next("/Login")
  }
})

export default router
